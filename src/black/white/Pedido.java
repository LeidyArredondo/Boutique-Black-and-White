    public double[] Sumatoria(JTextField c, JTextField v) {

        String cantidad, valor;
        double r[];
        r = new double[3];

        cantidad = c.getText();
        valor = v.getText();

        int can = Integer.parseInt(cantidad);
        int val = Integer.parseInt(valor);

        r[0] = can * val;
        r[1] = r[0] * 0.16;
        r[2] = r[0] + r[1];

        return r;
    }

