    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed

        String PerfilaConsultar = JOptionPane.showInputDialog(this, "Ingrese el código de la compra");
        nuevoPedido = listaRegistroPedido.buscarPedido(PerfilaConsultar);
        if (nuevoPedido == null) {

            JOptionPane.showMessageDialog(this, "El código de la compra no existe", "Gestionar Pedido - S.I.C", JOptionPane.OK_OPTION,
                    imaInforma);
        } else {
            this.LlenarDatos();
        }

    }//GEN-LAST:event_btnConsultarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        String pedidoaModificar = JOptionPane.showInputDialog(this, "Ingrese el código del producto");
        if (listaRegistroPedido.siExiste(pedidoaModificar)) {
            nuevoPedido = listaRegistroPedido.buscarPedido(pedidoaModificar);
            this.LlenarDatos();
            this.ActivarCampos();
            swModificar = 1;
            this.desactivarBotones();
        } else {

            JOptionPane.showMessageDialog(this, "El código de la compra ingresado no esta registrado ", "Gestionar Pedido - S.I.C", JOptionPane.OK_OPTION,
                    imaInforma);

        }
        txtCodCompra.setEnabled(false);
        
    }//GEN-LAST:event_btnModificarActionPerformed

