    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        nuevoPedido = this.GuardarCampos();
        if (this.validacionObligatorios().equals("")) {
            if (swModificar == 1) {
                if (listaRegistroPedido.guardarModificacionPedido(nuevoPedido)) {
                    JOptionPane.showMessageDialog(this, "Se modificó el pedido correctamente", "Gestionar Pedido - S.I.C", JOptionPane.OK_OPTION,
                            imaInforma);
                    this.LimpiarCampos();
                    this.DesactivarCampos();
                    swModificar = 0;
                    btnGuardar.setEnabled(false);
                    this.activarBotones();
                } else {
                    JOptionPane.showMessageDialog(this, "No se pudo modificar el pedido correctamente", "Gestionar Pedido - S.I.C", JOptionPane.OK_OPTION,
                            imaInforma);

                }
            } else {
                if (listaRegistroPedido.siExiste(nuevoPedido.getCodCompr())) {

                    JOptionPane.showMessageDialog(this, "El Pedido ya existe", "Gestionar Pedido - S.I.C", JOptionPane.OK_OPTION,
                            imaInforma);

                } else {

                    if (listaRegistroPedido.GuardarRegistroPedido(nuevoPedido)) {
                        JOptionPane.showMessageDialog(this, "Se guardó el pedido correctamente", "Gestionar Pedido - S.I.C", JOptionPane.OK_OPTION,
                                imaInforma);
                         btnGuardar.setEnabled(false);
                         this.activarBotones();
                    } else {
                        JOptionPane.showMessageDialog(this, "No pudo ser guardado ", "Gestionar Pedido - S.I.C", JOptionPane.OK_OPTION,
                                imaInforma);
                    }
                    this.LimpiarCampos();
                    this.DesactivarCampos();
                }
            }

        } else {

            JOptionPane.showMessageDialog(this, "Ingrese los campos Obligatorios", "Gestionar Pedido - S.I.C", JOptionPane.OK_OPTION,
                    imaInforma);
        }

    }//GEN-LAST:event_btnGuardarActionPerformed

